                    <h2><b>What is on a DUPRO checklist?</b></h2>
                    <p><span style="font-weight: 400;">A DUPRO checklist typically includes the following
                            items:</span></p>
                    <ul>
                        <li style="font-weight: 400;" aria-level="1"><span
                                style="font-weight: 400;">Production status: Check the progress and capacity
                                and whether the production schedule is on track.</span></li>
                        <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Product
                                specifications: Verify that the products are being manufactured according to
                                the original specifications, such as size, colour, weight, material, and
                                design.</span></li>
                        <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Product
                                quality: Inspect the products for any defects, such as functional,
                                aesthetic, performance, or safety issues.</span></li>
                        <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Packing
                                and packaging: Check the packing and packaging of the products, including
                                unit packing, inner packing, outer packing, and export cartons.</span></li>
                        <li style="font-weight: 400;" aria-level="1"><span
                                style="font-weight: 400;">Compliance with quality standards: Verify that the
                                products are manufactured according to the agreed-upon quality
                                standards.</span></li>
                    </ul>
