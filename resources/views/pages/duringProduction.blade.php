                    <h2><b>What is a during-production (DUPRO) inspection?</b></h2>
                    <p><strong>DUPRO (During production inspection</strong>) is a quality control inspection
                        that occurs while production is underway, usually when 20-80% of the production is
                        completed and packed for shipping. Its purpose is to check that the factory follows
                        the original specifications correctly and that the output is still on schedule.
                        DUPRO (During production inspection) can also help to detect and correct any issues
                        or defects that could affect the quality or delivery of the products.</p>
                    <h2><b>Why perform a DUPRO inspection?</b></h2>
                    <p><span style="font-weight: 400;">DUPRO inspection is an essential part of ensuring
                            product quality and timely delivery. By identifying any issues or defects early
                            in the manufacturing process, corrective actions can be taken to prevent any
                            further problems from arising. This can save time and money and prevent the need
                            for costly rework or product recalls.</span></p>
                    <h2><b>What are the steps to perform DUPRO at VIS?</b></h2>
