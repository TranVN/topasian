                        <p><span style="color: #000000;">(RoHS) is a set of EU regulations that implements EU Directive 2002/95 that restricts the use of hazardous substances in electrical and electronic equipment. This directive bans placing on the EU market, any product having an electrical/electronic component containing more than thresholds set for lead, cadmium, mercury, hexavalent chromium, polybrominated biphenyl (PBB) and polybrominateddiphenyl ether (PBDE) flame retardants.
