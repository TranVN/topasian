                    <p><span style="font-weight: 400;">Here are the typical steps involved in conducting
                            Loading/Unloading Supervision:</span></p>
                    <p><span style="font-weight: 400;">I<em>nspection agency selection:</em> The first step
                            in conducting Loading/Unloading Supervision (LS) is to select a reputable
                            inspection agency with experience in this field. The agency should know the
                            relevant regulations, product standards, and customs procedures.</span></p>
                    <p><span style="font-weight: 400;"><em>Inspection visit:</em> The inspector will visit
                            the loading or unloading site and visually inspect the goods. This includes
                            verifying the quantity of goods, checking the product specifications, and
                            ensuring that the packing and packaging meet the required standards.</span></p>
                    <p><span style="font-weight: 400;"><em>Sampling standard:</em> If necessary, the
                            inspector may take a sample of the goods to test for quality and compliance with
                            relevant standards. The sampling standard will depend on the type of product
                            being transported and the applicable regulations.</span></p>
                    <p><span style="font-weight: 400;"><em>Product quality check:</em> The inspector will
                            also conduct a quality check of the goods to ensure they are free from defects
                            or damage. Any defects or issues will be recorded in a defects report, which
                            will be shared with the buyer and supplier.</span></p>
                    <p><span style="font-weight: 400;"><em>Labels and markings:</em> The inspector will
                            verify that the goods are properly labelled and marked with the correct
                            information, such as the product name, origin, and destination.&nbsp;</span></p>
                    <p><span style="font-weight: 400;"><em>Customs declaration: </em>The inspector will
                            ensure that the goods are properly declared to customs and that all required
                            documentation, such as the packing list and invoice, is accurate and
                            complete.</span></p>
                    <p><span style="font-weight: 400;"><em>Report issuance:</em> The inspector will issue a
                            report detailing the inspection results after the inspection. This report will
                            include information about the quantity and quality of the goods, any defects or
                            issues, and any other relevant information.</span></p>
                    <p><span style="font-weight: 400;"><em>Certificate verification:</em> Finally, the
                            inspector may verify any certificates or other documentation related to the
                            goods, such as certificates of origin or quality.</span></p>
