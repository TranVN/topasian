                        <h2><b>What are the types of Quality Inspection?</b></h2>
                        <p><span data-preserver-spaces="true">There are several types of quality inspections,
                                including Pre-production Inspection, DUPRO (During Production Inspection),
                                Pre-shipment Inspection, Loading Supervision, Full Inspection (100% Inspection),
                                and Sample Checking. Each of these inspections uniquely ensures that the
                                products meet the required quality standards and specifications.&nbsp;</span></p>
                        <ol>
                            <li><span data-preserver-spaces="true"><a
                                        href="https://vnvis.com/pre-production-inspection/">Pre-production
                                        Inspection</a>: This Inspection takes place before production begins and
                                    involves checking raw materials, production methods, and quality control
                                    procedures to ensure that the production process will meet the required
                                    quality standards.</span></li>
                            <li><span data-preserver-spaces="true"><a href="https://vnvis.com/dupro/">DUPRO</a>
                                    (During Production Inspection): DUPRO inspections are conducted during the
                                    production process to identify any defects and prevent any further
                                    production of defective goods. This Inspection can help reduce the number of
                                    faulty products and ensure that the production process is proceeding as
                                    planned.</span></li>
                            <li><span data-preserver-spaces="true"><a
                                        href="https://vnvis.com/preshipment-inspection/">Pre-shipment
                                        Inspection:</a> This Inspection takes place after production is complete
                                    and before the goods are shipped to the customer. The goal is to ensure that
                                    the products meet the quality standards the customer and supplier agreed
                                    upon.</span></li>
                            <li><span data-preserver-spaces="true"><a
                                        href="https://vnvis.com/loading-unloading-supervision/">Loading
                                        Supervision</a>: Loading supervision involves overseeing goods loading
                                    onto the vehicle to ensure that the products are handled carefully, packaged
                                    correctly, and packed securely to prevent damage during
                                    transportation.</span></li>
                            <li><span data-preserver-spaces="true"><a href="https://vnvis.com/full-inspection/">Full
                                        Inspection (100%
                                        Inspection)</a>: This Inspection involves checking every item produced
                                    to ensure that all products meet the required quality standards. This type
                                    of Inspection is usually conducted for high-value or critical
                                    products.</span></li>
                            <li><span data-preserver-spaces="true"><a
                                        href="https://vnvis.com/sample-picking-service/">Sample Picking:</a>
                                    This Inspection involves checking a random sample of goods to ensure they
                                    meet the required quality standards. This type of Inspection is usually
                                    conducted for products with an extensive production quantity, where checking
                                    each item is not feasible.</span></li>
                        </ol>
