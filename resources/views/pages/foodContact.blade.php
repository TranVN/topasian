                        <h5>What is California Proposition 65?</h5>
                        <p><span style="color: #000000;">The Safe Drinking Water and Toxic Enforcement Act of 1986, often referred to as Cal Prop 65, is a California law intended to protect the State’s residents and drinking water sources from toxic chemicals and to inform its citizens about exposure to them. The State published a list of naturally occurring and synthetic chemicals that are known to cause cancer, birth defects or other reproductive harm. That list, which must be updated and published once a year, has grown to include more than 950 chemicals since its inception. The two main categories of the listed chemicals are carcinogens and reproductive toxicants.
                        <br>
                        While Cal Prop 65 does not expressly prohibit the sale of products containing its listed chemicals, it does require the manufacturer to provide a "clear and reasonable" warning if a product’s content of a listed chemical exceeds the "safe harbor level." Of the 950+ chemicals regulated by Prop 65, safe harbor levels have only been identified for about 300.</span></p>

                        <ul>
                            <li> Apparel and textiles</li>
                            <li> Footwear and accessories</li>
                            <li>Eyewear </li>
                            <li>Toys and children’s products</li>
                            <li>Cosmetics and beauty products</li>
                            <li>Furniture</li>
                            <li>Electrical and electronic goods</li>
                            <li>Promotional products</li>
                        </ul>
                        <p><span style="color: #000000;">If a product sold in California is found to contain a chemical listed under Prop 65 without the "clear and reasonable warning" required by this regulation, the manufacturer is issued a 60-day Notice of Violation. During the 60-day period, the California Attorney General’s office can take legal action against the manufacturer, and if no such action is taken, a private party can file a lawsuit after the 60-day period has expired.
                        <br>
                        Historically, an average settlement for a Prop 65 lawsuit is around $65,000. In lawsuits involving large groups of retailers and manufacturers, settlements can exceed $1,000,000.
                        <br>
                        Given the high costs and reputational damage of Prop 65 lawsuits, more and more brands and retailers are taking a proactive approach to Prop 65 compliance, and ensure that their products meet all the relevant requirements.</span></p>

                       <h5> The Impact of Consumer Product Recalls</h5>
                        <p><span style="color: #000000;">
                        As most manufacturers are very aware, the CPSIA came into effect in August 2008, and included many new mandatory regulations and testing requirements for toys and children’s products such as lead and phthalate content, reasonable testing programs, and third party testing. Manufacturers, importers, distributors and retailers of consumer goods must ensure that their products comply with the CPSIA. Further to the requirements under the CPSIA was the creation of SaferProducts.gov, a publically-searchable database of reports of harm.
                        <br>
                        Product recalls continue to be a costly issue for manufacturers, and the need for a rigorous testing and inspection program is clear. In FY 2015, the Consumer Product Safety Commission listed over 230 consumer product recalls on their website with a variety of hazards from burns and strangulation to lead paint and laceration.
                        <br>
                        A significant decline in future sales as a result of a product recall could have a huge impact on a business as a whole. In addition to the costs associated with conducting the recall itself (investigation costs, customer reimbursement, professional feeds, transportation fees, disposal fees, etc), there are many other factors that can contribute to long-term financial impact such as:
                        </span></p>

                        <ul>
                            <li>Reduced sales because of lost product confidence</li>
                            <li>Time needed to regain market share</li>
                            <li>Possibility that market share may not reach the same levels as before the recall</li>
                            <li>Increased marketing expenses to regain lost market share</li>
                        </ul>
                        <p><span style="color: #000000;">
                        The impact on a company’s brand is synonymous with an impact on its finances. In an instant, a reputation and brand that has taken years for a company to build can be tarnished. The digital nature of our current markets also increases the impact that a recall can have. Information is available almost instantaneously and can be distribution over global consumer markets quickly – this means the impact on a brand in the face of a recall can be even more immediate and catastrophic. Product recalls can do serious damage to a brand, and can do it very quickly.
